    private void listaMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listaMouseReleased
        if(lista.getSelectedRow()>=0){
            int f = lista.getSelectedRow();
            cod = Integer.parseInt((String)lista.getValueAt(f,0));
            try {
                r = getS().executeQuery("SELECT descripcion,codcama,codtarifa,numcama,"
                        + "tvcable,wifi,aireacondicionado,terma,jacuzzi FROM tipohabitacion"
                        + " WHERE codtipoh="+cod);
                while(r.next()){
                    jTextField1.setText(r.getString(1));
                    jComboBox1.setSelectedItem(r.getString(2));
                    jComboBox2.setSelectedItem(r.getString(3));
                    jSpinner1.setValue(r.getInt(4));
                    tv.setSelectedIndex(r.getInt(5));
                    wifi.setSelectedIndex(r.getInt(6));
                    aire.setSelectedIndex(r.getInt(7));
                    terma.setSelectedIndex(r.getInt(8));
                    jacuzzi.setSelectedIndex(r.getInt(9));
                }
            } catch (SQLException ex) {
                Logger.getLogger(Tipo_Habitacion.class.getName()).log(Level.SEVERE, null, ex);
            }
            cancelar.setVisible(true);  
            activo.setEnabled(true);
            agregar.setText("Guardar");
            String estado = (String)lista.getValueAt(f,2);
            if((estado.equals("Activo"))){
                activo.setText("Inactivo");
            } else if(estado.equals("Inactivo")){
                activo.setText("Activo");
            }
        }
    }//GEN-LAST:event_listaMouseReleased

