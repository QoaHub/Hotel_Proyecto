    public void Lista_th() throws SQLException{
        String[] datos = new String[3];
        int f=l.getRowCount();
        if(f>0) {
            for(int i=0;i<f;i++) {
                l.removeRow(0);
            }
        }
        r = getS().executeQuery("SELECT codtipoh,descripcion,"
                            + "(CASE    WHEN estado=0 THEN 'Activo'"
                            + "         WHEN estado=1 THEN 'Inactivo'"
                            + " END)  AS estado FROM tipohabitacion");
        while(r.next()) {
            datos[0] = (String)r.getString(1);
            datos[1] = (String)r.getString(2);
            datos[2] = (String)r.getString(3);
            l.addRow(datos);
        }
    }

